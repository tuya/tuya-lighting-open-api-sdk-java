package com.tuya.lighting.open.api.domain.group;

/**
 * Tiny group
 *
 * @author lighting
 */
public class TinyGroup {

    /**
     * id
     */
    private Long id;

    /**
     * name
     */
    private String name;

    /**
     * product id
     */
    private String productId;

    /**
     * category
     */
    private String category;

    /**
     * group type
     */
    private Integer groupType;

    /**
     * type
     */
    private Integer type;

    /**
     * local id
     */
    private String localId;

    /**
     * mesh id
     */
    private String meshId;

    /**
     * icon url
     */
    private String iconUrl;

    /**
     * device number
     */
    private Integer deviceNum;

    /**
     * local key
     */
    private String localKey;

    /**
     * master node id
     */
    private String masterNodeId;

    /**
     * master device id
     */
    private String masterDevId;

    /**
     * pv
     */
    private String pv;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public Integer getGroupType() {
        return groupType;
    }

    public void setGroupType(Integer groupType) {
        this.groupType = groupType;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getLocalId() {
        return localId;
    }

    public void setLocalId(String localId) {
        this.localId = localId;
    }

    public String getMeshId() {
        return meshId;
    }

    public void setMeshId(String meshId) {
        this.meshId = meshId;
    }

    public String getIconUrl() {
        return iconUrl;
    }

    public void setIconUrl(String iconUrl) {
        this.iconUrl = iconUrl;
    }

    public Integer getDeviceNum() {
        return deviceNum;
    }

    public void setDeviceNum(Integer deviceNum) {
        this.deviceNum = deviceNum;
    }

    public String getLocalKey() {
        return localKey;
    }

    public void setLocalKey(String localKey) {
        this.localKey = localKey;
    }

    public String getMasterNodeId() {
        return masterNodeId;
    }

    public void setMasterNodeId(String masterNodeId) {
        this.masterNodeId = masterNodeId;
    }

    public String getMasterDevId() {
        return masterDevId;
    }

    public void setMasterDevId(String masterDevId) {
        this.masterDevId = masterDevId;
    }

    public String getPv() {
        return pv;
    }

    public void setPv(String pv) {
        this.pv = pv;
    }
}
